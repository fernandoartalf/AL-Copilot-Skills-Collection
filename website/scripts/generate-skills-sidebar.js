/**
 * Reads root skills/ folder and generates sidebarsSkills.ts
 * with explicit entries for each skill and its references.
 *
 * Run: node scripts/generate-skills-sidebar.js
 */

const fs = require('fs');
const path = require('path');

const SKILLS_DIR = path.join(__dirname, '..', '..', 'skills');
const OUTPUT_FILE = path.join(__dirname, '..', 'sidebarsSkills.ts');

/** Read the first H1 heading from a markdown file */
function getH1(filePath) {
  if (!fs.existsSync(filePath)) return null;
  const content = fs.readFileSync(filePath, 'utf-8');
  const match = content.match(/^# (.+)$/m);
  return match ? match[1].trim() : null;
}

/** Strip .md extension from a filename */
function stripExt(name) {
  return name.replace(/\.md$/, '');
}

/** Convert kebab-case filename to Title Case label */
function toLabel(name) {
  return stripExt(name)
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function main() {
  const skillFolders = fs
    .readdirSync(SKILLS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  const items = [];

  for (const folder of skillFolders) {
    const skillDir = path.join(SKILLS_DIR, folder);
    const skillMd = path.join(skillDir, 'SKILL.md');
    if (!fs.existsSync(skillMd)) continue;

    const displayName = getH1(skillMd) || folder;

    // Check for references
    const refsDir = path.join(skillDir, 'references');
    let refItems = [];
    if (fs.existsSync(refsDir)) {
      refItems = fs
        .readdirSync(refsDir)
        .filter((f) => f.endsWith('.md'))
        .sort()
        .map((f) => ({
          type: 'doc',
          id: `${folder}/references/${stripExt(f)}`,
          label: getH1(path.join(refsDir, f)) || toLabel(f),
        }));
    }

    if (refItems.length > 0) {
      items.push({
        type: 'category',
        label: displayName,
        link: { type: 'doc', id: `${folder}/SKILL` },
        collapsed: true,
        items: refItems,
      });
    } else {
      items.push({
        type: 'doc',
        id: `${folder}/SKILL`,
        label: displayName,
      });
    }
  }

  // Build the sidebar TS source
  const sidebarItems = JSON.stringify(
    [
      { type: 'doc', id: 'overview', label: 'Skills Overview' },
      ...items,
    ],
    null,
    4,
  );

  const tsContent = `// Auto-generated by scripts/generate-skills-sidebar.js — do not edit manually.
import type {SidebarsConfig} from '@docusaurus/plugin-content-docs';

const sidebarsSkills: SidebarsConfig = {
  skillsSidebar: ${sidebarItems.replace(/"(\w+)":/g, '$1:').replace(/"/g, "'")},
};

export default sidebarsSkills;
`;

  fs.writeFileSync(OUTPUT_FILE, tsContent);
  console.log(
    `Generated ${OUTPUT_FILE} with ${items.length} skill(s).`,
  );
}

main();
