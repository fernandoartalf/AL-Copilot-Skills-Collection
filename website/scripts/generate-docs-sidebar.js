/**
 * Generates sidebars.ts with integrated skills structure.
 * Reads docs/skills/ folder and creates collapsible categories.
 *
 * Run: node scripts/generate-docs-sidebar.js
 */

const fs = require('fs');
const path = require('path');

const SKILLS_DIR = path.join(__dirname, '..', 'docs', 'skills');
const INSTRUCTIONS_DIR = path.join(__dirname, '..', 'docs', 'instructions');
const OUTPUT_FILE = path.join(__dirname, '..', 'sidebars.ts');

/** Read the first H1 heading from a markdown file */
function getH1(filePath) {
  if (!fs.existsSync(filePath)) return null;
  const content = fs.readFileSync(filePath, 'utf-8');
  const match = content.match(/^# (.+)$/m);
  return match ? match[1].trim() : null;
}

/** Strip .md extension from a filename */
function stripExt(name) {
  return name.replace(/\.md$/, '');
}

/** Convert kebab-case filename to Title Case label */
function toLabel(name) {
  return stripExt(name)
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function generateSkillsItems() {
  if (!fs.existsSync(SKILLS_DIR)) {
    return [];
  }

  const skillFolders = fs
    .readdirSync(SKILLS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  const items = [];

  for (const folder of skillFolders) {
    const skillDir = path.join(SKILLS_DIR, folder);
    const skillMd = path.join(skillDir, 'SKILL.md');
    if (!fs.existsSync(skillMd)) continue;

    const displayName = getH1(skillMd) || folder;

    // Check for references
    const refsDir = path.join(skillDir, 'references');
    let refItems = [];
    if (fs.existsSync(refsDir)) {
      refItems = fs
        .readdirSync(refsDir)
        .filter((f) => f.endsWith('.md'))
        .sort()
        .map((f) => ({
          type: 'doc',
          id: `skills/${folder}/references/${stripExt(f)}`,
          label: getH1(path.join(refsDir, f)) || toLabel(f),
        }));
    }

    if (refItems.length > 0) {
      items.push({
        type: 'category',
        label: displayName,
        link: { type: 'doc', id: `skills/${folder}/SKILL` },
        collapsed: true,
        items: refItems,
      });
    } else {
      items.push({
        type: 'doc',
        id: `skills/${folder}/SKILL`,
        label: displayName,
      });
    }
  }

  return items;
}

function generateInstructionsItems() {
  if (!fs.existsSync(INSTRUCTIONS_DIR)) {
    return [];
  }

  const instructionFiles = fs
    .readdirSync(INSTRUCTIONS_DIR)
    .filter((f) => f.endsWith('.md') && f !== 'README.md')
    .sort();

  const items = instructionFiles.map((file) => {
    const filePath = path.join(INSTRUCTIONS_DIR, file);
    const displayName = getH1(filePath) || toLabel(file);
    return {
      type: 'doc',
      id: `instructions/${stripExt(file)}`,
      label: displayName,
    };
  });

  return items;
}

function main() {
  const skillsItems = generateSkillsItems();
  const instructionsItems = generateInstructionsItems();

  const sidebar = {
    tutorialSidebar: [
      'intro',
      {
        type: 'category',
        label: 'Skills',
        collapsed: true,
        items: skillsItems,
      },
      {
        type: 'category',
        label: 'Instructions',
        collapsed: true,
        items: instructionsItems,
      },
      {
        type: 'category',
        label: 'Release Notes',
        collapsed: true,
        items: [
          {
            type: 'category',
            label: '2026',
            collapsed: true,
            items: [
              { type: 'doc', id: 'releasenotes/2026/april', label: 'April 2026' },
            ],
          },
        ],
      },
      'contributing',
      'SECURITY',
      'LICENSE',
    ],
  };

  const tsContent = `// Auto-generated by scripts/generate-docs-sidebar.js — do not edit manually.
import type {SidebarsConfig} from '@docusaurus/plugin-content-docs';

const sidebars: SidebarsConfig = ${JSON.stringify(sidebar, null, 2)
    .replace(/"(\w+)":/g, '$1:')
    .replace(/"/g, "'")};

export default sidebars;
`;

  fs.writeFileSync(OUTPUT_FILE, tsContent);
  console.log(
    `Generated ${OUTPUT_FILE} with ${skillsItems.length} skill(s).`,
  );
}

main();
